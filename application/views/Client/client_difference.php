	<p>Major partner schools of the Education Concern Center (ECC) include:</p>

 	<h4 class="ui left header">Bhubaneshwori Secondary School</h4>
 	<p>Located in Dhading, Nepal, Bhubaneshwori Secondary School inclues 15 teachers and 500 students. With rapidly growing community support, it is expected that more than 100 students will enroll in this school from neighboring schools due to the improved physical facilities and academics. This is an example of the impact ECC partnership with this school has had. ECC continues to focus on training and managerial support at this time.
	</p>

	<h4 class="ui left header">Sepat Siranchour Secondary School</h4>
 	<p>Located in Syangja, Nepal, this school has 11 teachers and 356 students. The majority of the students are this school are considered from the Dalit caste, members of the poorest and most disadvantaged community. ECC has provided support for the school library and purchased uniforms for some of the students. ECC volunteers attend meetings and are involved in management and decision-making at the school.</p>

 	<h4 class="ui left header">Krishna Primary School</h4>
 	<p>Located in Gamauli, Nepal, ECC has been working in partnership with the local community to rebuild the school toilets. ECC was also able to secure funding to help the community build a water pipeline from the local market so that students now have access to fresh water at the school for the very first time in school history. ECC continues to provide teacher training and parent education to the students at this school. The school is already realizing improved attendance rates due to the interventions to date.</p>
