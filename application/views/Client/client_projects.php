<div class="ui header centered">Shree Krishna Primary School Gamauli, Dailekh<br>
School feeding program<br><br>Background</div>
<p class="ui justified">Krishna Primary School is located in a remote area in western Nepal which is corridor of least development region called Karnali. No more development organizations are working in this district in the area. Most of pupils are from disadvantaged (19% Dalits/disadvantaged people are in this district and 45% dalits/disadvantaged are in this school. There are about 101 students in that school.  Most of children in grade one leave school after one month they enroll in school. The drop out rate and repetition rate is high till now and we can se little bit increment in promotion rate. Community people are being involved in these days which is very important result of teacher training, Friday program, library, and supporting materials to that school.</p><p>
Children arrive at school by 9.30 am, it mean they eat their lunch around 8.30 am in the morning. they get hunger after 11/12 o clock. So that they cannot stay up to last of school time which is 4.pm.  Something to eat after few hours is good for children when they are growing. But this situation is just opposite in Nepal. In their growing age they do not have eating opportunity till 7/8 hours gap. Students cannot learn with their empty stomach.  This is the usual scenario in Nepal. One time day meal is very important in schools in Nepal either parents provide themselves or the school provides.
</p><p> 
Here my ideas to address this issue as a sustainable way is to start the Friday program as a key involvement of parent and empower the parents in a productive way and then provide them leadership to handle the day meal program in their respective schools. Later then parents contribute for the day meal. During this day meal program, use local production and encourage parents to product food and to sell in school. When parents start to product in sufficient level then it will be easier to provide for school for their children.
</p><p>
This low cost and very efficient program will be replicate in many other schools in Nepal.  </p>
<h4>Objective</h4>
<ol>
<li>To retain students in school</li>
<li>To reduce drop out rate and repetition rate</li>
<li>To increase promotion rate and achievement score</li>
<li>To improve the parents and community peoples' participation in school</li>
<li>Empowering women and improving their life standerd</li>
</ol>
<p class="ui justified"><b>Methodology: </b> Every Friday, the government provision is half day work in school. So that schools closed by 1 pm. But Krishna primary school organize different programs for the students, they perform among in different groups. So that they stay by 4.00 pm and finally they get snacks which a kind of incentive for students they are conditioned to attend school every day.  This Friday meal will be conditioning for students to be regular in their schools and also conditioning for parents to visit schools and to take responsibility of their child and education. </p>
